def _matches_any(path: str, patterns: list[str]) -> bool:
    for pattern in patterns:
        if (
            fnmatch.fnmatch(path, pattern)
            or fnmatch.fnmatch(os.path.basename(path), pattern)
            or pattern in path.split(os.sep)
        ):
            return True
    return False
