def plan_docs(scan: RepoScan, cfg: dict[str, Any], llm: LLMClient) -> DocPlan:
    """Run the multi-step planner.

    Phase 2 scans (giant-file clustering, endpoint bundles, integration discovery)
    run first to enrich the scan, then:
    Step 1: CLASSIFY — categorize every file/artifact
    Step 2: PROPOSE — create bucket candidates (no cap, LLM decides freely)
    Step 3: ASSIGN — map files to buckets, produce final plan
    Step 4: AUTO-GEN endpoint_ref buckets from scan data (one per endpoint)
    """
    from rich.live import Live
    from rich.text import Text

    # ── Phase 2 Scans (enrich before planning) ───────────────────────────
    console.print(Panel("[bold]Running Phase 2 scan upgrades[/bold]", border_style="green"))
    scan = run_phase2_scans(scan, cfg, llm)

    max_pages = cfg.get("max_pages", 0)
    giant_file_threshold = cfg.get("giant_file_lines", 2000)

    # Build shared context strings
    file_tree_str = _format_file_tree_compressed(scan.file_tree, scan.file_summaries)
    file_summaries_str = _format_summaries_compressed(scan.file_summaries)
    endpoints_str = _format_endpoints(scan.api_endpoints)
    lang_str = ", ".join(f"{lang} ({count})" for lang, count in sorted(scan.languages.items(), key=lambda x: -x[1]))
    fw_str = ", ".join(scan.frameworks_detected) or "none detected"

    # ── Step 1: Classify ─────────────────────────────────────────────────
    console.print(Panel("[bold]Planner Step 1/3: Classifying repository artifacts[/bold]", border_style="cyan"))

    classify_prompt = CLASSIFY_PROMPT.format(
        languages=lang_str,
        frameworks=fw_str,
        total_files=scan.total_files,
        endpoint_count=len(scan.api_endpoints),
        entry_points=", ".join(scan.entry_points[:10]) or "none",
        config_files=", ".join(scan.config_files[:15]) or "none",
        file_tree=file_tree_str[:15000],
        file_summaries=file_summaries_str[:20000],
        endpoints=endpoints_str[:5000],
        giant_file_threshold=giant_file_threshold,
    )

    classification = _llm_step(llm, CLASSIFY_SYSTEM, classify_prompt, "classify")
    if not classification:
        console.print("[yellow]⚠ Classification failed — falling back to auto-plan[/yellow]")
        return _fallback_plan(scan, cfg)

    _print_classification_summary(classification)

    # ── Step 2: Propose Buckets ──────────────────────────────────────────
    console.print(Panel("[bold]Planner Step 2/3: Proposing documentation buckets[/bold]", border_style="cyan"))

    # Build classification summary for the proposal step
    classification_summary = _build_classification_summary(classification)

    # Enrich with Phase 2 results if available
    if scan.integration_identities:
        integration_signals = "## Discovered Integration Identities (Phase 2)\n"
        for ident in scan.integration_identities:
            substantial_tag = "[SUBSTANTIAL — create standalone page]" if ident.is_substantial else "[EMBEDDED — keep in feature pages]"
            integration_signals += f"- **{ident.display_name}** ({ident.name}) {substantial_tag}\n"
            integration_signals += f"  Files: {', '.join(ident.files[:5])}\n"
            integration_signals += f"  Evidence: {', '.join(ident.evidence[:3])}\n"
    else:
        integration_signals = json.dumps(classification.get("integration_signals", []), indent=2)

    cross_cutting = json.dumps(classification.get("cross_cutting", []), indent=2)

    # Enrich giant files with cluster info from Phase 2
    giant_files_list = classification.get("giant_files", [])
    giant_parts = []
    for f in giant_files_list:
        lc = scan.file_line_counts.get(f, '?')
        cluster_info = ""
        if f in scan.giant_file_clusters:
            analysis = scan.giant_file_clusters[f]
            cluster_names = [c.cluster_name for c in analysis.clusters]
            cluster_info = f" → clusters: {', '.join(cluster_names)}"
        giant_parts.append(f"- {f} ({lc} lines){cluster_info}")
    giant_files_str = "\n".join(giant_parts) or "(none)"

    # Build database info from artifact_scan
    database_info = "(none detected)"
    if scan.artifact_scan and hasattr(scan.artifact_scan, "database_scan") and scan.artifact_scan.database_scan:
        db_scan = scan.artifact_scan.database_scan
        db_parts = [f"ORM: {db_scan.orm_framework or 'unknown'}", f"Total models: {db_scan.total_models}"]
        for mf in db_scan.model_files[:20]:
            if not mf.is_migration:
                models_str = ", ".join(mf.model_names[:10]) if mf.model_names else "?"
                db_parts.append(f"  - {mf.file_path}: [{models_str}]")
        if db_scan.migration_files:
            db_parts.append(f"  Migrations: {len(db_scan.migration_files)} files")
        if db_scan.schema_files:
            db_parts.append(f"  Schema files: {', '.join(db_scan.schema_files)}")
        database_info = "\n".join(db_parts)

    # Build max_pages instruction: 0 = no cap
    if max_pages and max_pages > 0:
        max_pages_instruction = f"- Maximum total buckets: {max_pages}"
    else:
        max_pages_instruction = (
            "- No hard limit on bucket count. Create as many buckets as needed for "
            "thorough, deep documentation. Quality and completeness matter most."
        )

    propose_prompt = PROPOSE_PROMPT.format(
        classification_summary=classification_summary,
        endpoint_count=len(scan.api_endpoints),
        endpoints=endpoints_str[:15000],
        integration_signals=integration_signals,
        cross_cutting=cross_cutting,
        giant_files=giant_files_str,
        database_info=database_info,
        max_pages_instruction=max_pages_instruction,
    )

    proposal = _llm_step(llm, PROPOSE_SYSTEM, propose_prompt, "propose")
    if not proposal:
        console.print("[yellow]⚠ Bucket proposal failed — falling back to auto-plan[/yellow]")
        return _fallback_plan(scan, cfg)

    _print_proposal_summary(proposal)

    # ── Step 3: Assign Files ─────────────────────────────────────────────
    console.print(Panel("[bold]Planner Step 3/3: Assigning files to buckets[/bold]", border_style="cyan"))

    proposed_buckets_str = json.dumps(proposal.get("buckets", []), indent=2)
    all_files_str = "\n".join(f"- {f}" for f in sorted(scan.file_summaries.keys()))
    setup_artifacts_str = "\n".join(f"- {f}" for f in scan.config_files) or "(none)"

    assign_prompt = ASSIGN_PROMPT.format(
        proposed_buckets=proposed_buckets_str[:15000],
        all_files=all_files_str[:12000],
        endpoints=endpoints_str[:3000],
        giant_files=giant_files_str,
        setup_artifacts=setup_artifacts_str,
    )

    assignment = _llm_step(llm, ASSIGN_SYSTEM, assign_prompt, "assign")
    if not assignment:
        console.print("[yellow]⚠ Assignment failed — falling back to auto-plan[/yellow]")
        return _fallback_plan(scan, cfg)

    # ── Merge proposal + assignment into final plan ──────────────────────
    plan = _merge_plan(proposal, assignment, classification, scan)

    # ── Step 4: Auto-generate per-endpoint reference pages from scan data ─
    plan = _auto_generate_endpoint_refs(plan, scan)

    # Validate coverage
    console.print("[dim]Validating file coverage...[/dim]")
    plan = _validate_coverage(plan, scan)
    if plan.orphaned_files:
        console.print(f"[yellow]⚠ {len(plan.orphaned_files)} file(s) were unassigned → auto-assigned[/yellow]")

    _print_plan_summary(plan)
    return plan


def _llm_step(llm: LLMClient, system: str, prompt: str, step_name: str) -> dict | None:
    """Execute a single LLM planning step with error handling."""
    from rich.live import Live
    from rich.text import Text

    response = None
    with Live(
        Text(f"⠋ Running planner step: {step_name}... (may take 15-30s)", style="bold cyan"),
        console=console,
        refresh_per_second=10,
        transient=True,
    ):
        try:
            response = llm.complete(system, prompt)
        except Exception as e:
            console.print(f"[red]✗ LLM call failed for {step_name}: {e}[/red]")
            return None

    if not response:
        return None

    try:
        return _parse_json_response(response)
    except Exception as e:
        console.print(f"[red]✗ Could not parse {step_name} response: {e}[/red]")
        # Try to salvage — sometimes the LLM wraps JSON in markdown
        try:
            # Strip markdown fences and retry
            cleaned = response.strip()
            if cleaned.startswith("```"):
                lines = cleaned.splitlines()
                lines = [l for l in lines if not l.strip().startswith("```")]
                cleaned = "\n".join(lines)
            return json.loads(cleaned)
        except Exception:
            return None


def _parse_json_response(response: str) -> dict:
    """Extract JSON from LLM response, handling markdown fences."""
    text = response.strip()
    if text.startswith("```"):
        lines = text.splitlines()
        lines = [l for l in lines if not l.strip().startswith("```")]
        text = "\n".join(lines)
    return json.loads(text)


# ─────────────────────────────────────────────────────────────────────────────
# Merge proposal + assignment into final DocPlan
# ─────────────────────────────────────────────────────────────────────────────

def _merge_plan(
    proposal: dict,
    assignment: dict,
    classification: dict,
    scan: RepoScan,
) -> DocPlan:
    """Combine the proposal (bucket definitions) with the assignment (file mapping)."""
    # Index proposed buckets by slug
    proposed_by_slug: dict[str, dict] = {}
    for b in proposal.get("buckets", []):
        proposed_by_slug[b["slug"]] = b

    # Index assignments by slug
    assigned_by_slug: dict[str, dict] = {}
    for a in assignment.get("buckets", []):
        assigned_by_slug[a["slug"]] = a

    buckets: list[DocBucket] = []
    for slug, prop in proposed_by_slug.items():
        assign = assigned_by_slug.get(slug, {})

        bucket = DocBucket(
            bucket_type=prop.get("bucket_type", "feature"),
            title=prop.get("title", slug),
            slug=slug,
            section=prop.get("section", ""),
            description=prop.get("description", ""),
            depends_on=prop.get("depends_on", []),
            owned_files=assign.get("owned_files", prop.get("candidate_files", [])),
            owned_symbols=assign.get("owned_symbols", []),
            artifact_refs=assign.get("artifact_refs", []),
            required_sections=prop.get("required_sections", _default_sections(prop.get("bucket_type", "feature"))),
            required_diagrams=prop.get("required_diagrams", []),
            coverage_targets=prop.get("coverage_targets", []),
            priority=assign.get("priority", 0),
        )
        buckets.append(bucket)

    # Sort by priority
    buckets.sort(key=lambda b: b.priority)

    nav_structure = proposal.get("nav_structure", {})
    skipped_files = assignment.get("skipped_files", [])

    return DocPlan(
        buckets=buckets,
        nav_structure=nav_structure,
        skipped_files=skipped_files,
        classification=classification,
        integration_candidates=classification.get("integration_signals", []),
    )


def _auto_generate_endpoint_refs(plan: DocPlan, scan: RepoScan) -> DocPlan:
    """Auto-generate individual endpoint_ref buckets from scan API endpoints.

    This creates one page per API endpoint (e.g. GET /api/v1/orders) and nests
    each under its parent endpoint-family bucket. The LLM planner only creates
    family buckets (endpoint type) — this fills in the per-endpoint detail pages.
    """
    import re as _re

    if not scan.api_endpoints:
        return plan

    # Map endpoints to their family bucket by matching resource group
    family_buckets = [b for b in plan.buckets if b.bucket_type == "endpoint"]

    # Build resource → family bucket slug mapping
    def _resource_from_path(path: str) -> str:
        clean = _re.sub(r"^/(?:api/)?(?:v\d+/)?", "", path)
        parts_list = [p for p in clean.split("/") if p and not p.startswith(":") and not p.startswith("{")]
        return parts_list[0] if parts_list else "general"

    resource_to_family: dict[str, str] = {}
    for fb in family_buckets:
        # Derive resource from slug (e.g. "orders-api" → "orders")
        resource = fb.slug.replace("-api", "").replace("-", "_")
        resource_to_family[resource] = fb.slug
        # Also map without underscores
        resource_to_family[resource.replace("_", "-")] = fb.slug

    ref_buckets: list[DocBucket] = []
    existing_slugs = {b.slug for b in plan.buckets}

    for ep in scan.api_endpoints:
        method = ep.get("method", "GET").upper()
        path = ep.get("path", "/unknown")
        handler = ep.get("handler", "")
        ep_file = ep.get("file", "")

        # Build slug
        path_slug = _re.sub(r"[/:{}]+", "-", path).strip("-").lower()
        ref_slug = f"{method.lower()}-{path_slug}"

        # Avoid duplicates
        if ref_slug in existing_slugs:
            continue
        existing_slugs.add(ref_slug)

        # Find parent family bucket
        resource = _resource_from_path(path)
        parent_slug = resource_to_family.get(resource, resource_to_family.get(resource.replace("_", "-"), ""))

        ref_buckets.append(DocBucket(
            bucket_type="endpoint_ref",
            title=f"{method} {path}",
            slug=ref_slug,
            section="API Endpoints",
            description=f"API reference for {method} {path} — handler: {handler}",
            owned_files=[ep_file] if ep_file else [],
            owned_symbols=[handler] if handler else [],
            required_sections=_default_sections("endpoint_ref"),
            priority=25,
            depends_on=[parent_slug] if parent_slug else [],
        ))

    if ref_buckets:
        plan.buckets.extend(ref_buckets)

        # Group endpoint_refs by family in nav — nested under parent family title
        family_slug_to_title: dict[str, str] = {
            b.slug: b.title for b in plan.buckets if b.bucket_type == "endpoint"
        }
        family_refs: dict[str, list[str]] = defaultdict(list)
        ungrouped: list[str] = []
        for b in ref_buckets:
            parent = b.depends_on[0] if b.depends_on else ""
            if parent and parent in family_slug_to_title:
                family_refs[parent].append(b.slug)
            else:
                ungrouped.append(b.slug)

        # Build nested nav sections: "API Endpoints > Orders API" etc.
        for family_slug, ref_slugs in sorted(family_refs.items()):
            family_title = family_slug_to_title.get(family_slug, family_slug)
            section_key = f"API Endpoints > {family_title}"
            # Include the family overview page first, then individual endpoints
            plan.nav_structure[section_key] = [family_slug] + ref_slugs
            # Remove family slug from its original section so it's not duplicated
            for section_name, slugs in list(plan.nav_structure.items()):
                if section_name != section_key and family_slug in slugs:
                    slugs.remove(family_slug)

        if ungrouped:
            plan.nav_structure["API Endpoints > Other"] = ungrouped

        console.print(f"[green]✓ Auto-generated {len(ref_buckets)} individual endpoint reference pages[/green]")

    return plan


def _default_sections(bucket_type: str) -> list[str]:
    """Default required sections per bucket type."""
    if bucket_type == "system":
        return ["overview", "architecture", "key_components", "configuration", "diagrams"]
    elif bucket_type == "feature":
        return ["overview", "main_workflows", "participating_endpoints", "core_helpers",
                "state_transitions", "integrations_involved", "config_dependencies",
                "edge_cases", "diagrams"]
    elif bucket_type == "endpoint":
        return ["route_overview", "auth_validation", "execution_flow", "downstream_calls",
                "integrations_touched", "state_changes", "async_side_effects",
                "response_errors", "diagrams"]
    elif bucket_type == "integration":
        return ["what_it_does", "where_it_enters", "participating_features",
                "request_response_flow", "auth_configuration", "retry_failure_handling",
                "operational_gotchas", "diagrams"]
    elif bucket_type == "endpoint_ref":
        return ["endpoint_summary", "handler", "parameters", "request_body",
                "response_format", "error_responses", "auth", "sequence_diagram"]
    elif bucket_type == "database":
        return ["overview", "er_diagram", "table_definitions", "relationships",
                "migrations", "query_patterns", "configuration"]
    return ["overview", "details", "diagrams"]


# ─────────────────────────────────────────────────────────────────────────────
# Coverage validation
# ─────────────────────────────────────────────────────────────────────────────

def _validate_coverage(plan: DocPlan, scan: RepoScan) -> DocPlan:
    """Ensure all important source files are assigned to at least one bucket."""
    all_assigned: set[str] = set()
    for bucket in plan.buckets:
        all_assigned.update(bucket.owned_files)
    skipped = set(plan.skipped_files)

    all_source = set(scan.file_summaries.keys())
    orphaned = all_source - all_assigned - skipped

    if orphaned:
        orphan_groups: dict[str, list[str]] = defaultdict(list)
        for f in orphaned:
            parts = f.split("/")
            group = parts[0] if len(parts) > 1 else "root"
            orphan_groups[group].append(f)

        for group_name, files in orphan_groups.items():
            # Try to find an existing bucket that covers this directory
            matched = False
            for bucket in plan.buckets:
                if any(f.startswith(group_name + "/") for f in bucket.owned_files):
                    bucket.owned_files.extend(files)
                    matched = True
                    break

            if not matched:
                # Create a catch-all feature bucket
                plan.buckets.append(DocBucket(
                    bucket_type="feature",
                    title=f"{group_name.replace('_', ' ').replace('-', ' ').title()} Module",
                    slug=f"{group_name.lower().replace(' ', '-').replace('_', '-')}-module",
                    section="Modules",
                    description=f"Documentation for the {group_name} module",
                    owned_files=files,
                    required_sections=_default_sections("feature"),
                    priority=50,
                ))
                if "Modules" not in plan.nav_structure:
                    plan.nav_structure["Modules"] = []
                plan.nav_structure["Modules"].append(plan.buckets[-1].slug)

        plan.orphaned_files = sorted(orphaned)

    return plan


# ─────────────────────────────────────────────────────────────────────────────
# Fallback plan (if LLM fails at any step)
# ─────────────────────────────────────────────────────────────────────────────

def _fallback_plan(scan: RepoScan, cfg: dict[str, Any]) -> DocPlan:
    """Generate a reasonable bucket-based plan without LLM, using repo structure."""
    import re

    buckets: list[DocBucket] = []
    nav: dict[str, list[str]] = defaultdict(list)
    assigned_files: set[str] = set()

    # ── System bucket: Architecture/Overview ─────────────────────────────
    overview_files = scan.entry_points[:5] + scan.config_files[:3]
    buckets.append(DocBucket(
        bucket_type="system",
        title="Architecture & Overview",
        slug="architecture",
        section="Overview",
        description="Project overview, architecture, and high-level design",
        owned_files=overview_files,
        required_sections=_default_sections("system"),
        priority=0,
    ))
    nav["Overview"].append("architecture")
    assigned_files.update(overview_files)

    # ── System bucket: Setup ─────────────────────────────────────────────
    if scan.config_files:
        buckets.append(DocBucket(
            bucket_type="system",
            title="Setup & Configuration",
            slug="setup",
            section="Getting Started",
            description="Installation, environment variables, and configuration",
            owned_files=[],
            artifact_refs=scan.config_files[:10],
            required_sections=["overview", "prerequisites", "installation", "configuration",
                               "environment_variables", "verification"],
            priority=1,
        ))
        nav["Getting Started"].append("setup")

    # ── Categorize files ─────────────────────────────────────────────────
    ROLE_DIRS = {
        "middleware": ["middleware", "middlewares"],
        "models": ["model", "models", "schemas", "entities"],
        "routes": ["route", "routes", "router", "routers"],
        "controllers": ["controller", "controllers", "handlers"],
        "services": ["service", "services"],
        "utils": ["util", "utils", "helpers", "lib", "common"],
        "config": ["config", "configs", "settings"],
    }

    dir_to_role: dict[str, str] = {}
    for role, dir_names in ROLE_DIRS.items():
        for d in dir_names:
            dir_to_role[d] = role

    role_buckets_map: dict[str, list[str]] = defaultdict(list)
    domain_buckets_map: dict[str, list[str]] = defaultdict(list)

    for rel_path in scan.file_summaries:
        parts = rel_path.split("/")
        if any(p in ("tests", "test", "__tests__", "spec") for p in parts):
            continue

        matched_role = None
        for part in parts:
            if part.lower() in dir_to_role:
                matched_role = dir_to_role[part.lower()]
                break

        if matched_role:
            role_buckets_map[matched_role].append(rel_path)
        else:
            if len(parts) >= 3 and parts[0] in ("src", "api", "app", "pkg", "internal"):
                group = parts[1]
            elif len(parts) >= 2:
                group = parts[0]
            else:
                group = "root"
            domain_buckets_map[group].append(rel_path)

    # ── Database bucket: Schema & Models ────────────────────────────────
    model_files = role_buckets_map.get("models", [])
    # Also grab model files from database scan if available
    db_model_files: list[str] = list(model_files)
    if scan.artifact_scan and hasattr(scan.artifact_scan, "database_scan") and scan.artifact_scan.database_scan:
        db_scan = scan.artifact_scan.database_scan
        for mf in db_scan.model_files:
            if mf.file_path not in db_model_files:
                db_model_files.append(mf.file_path)
        db_model_files.extend(f for f in db_scan.schema_files if f not in db_model_files)
    if db_model_files:
        buckets.append(DocBucket(
            bucket_type="database",
            title="Database & Schema",
            slug="database-schema",
            section="Database",
            description="Database schemas, models, ER diagrams, relationships, and migrations",
            owned_files=db_model_files,
            required_sections=_default_sections("database"),
            required_diagrams=["er_diagram"],
            priority=3,
        ))
        nav["Database"].append("database-schema")
        assigned_files.update(db_model_files)

    # ── System bucket: Middleware & Auth ──────────────────────────────────
    mw_files = role_buckets_map.get("middleware", [])
    if mw_files:
        buckets.append(DocBucket(
            bucket_type="system",
            title="Middleware & Authentication",
            slug="middleware-auth",
            section="Architecture",
            description="Authentication, authorization, rate limiting, and middleware pipeline",
            owned_files=mw_files,
            required_sections=_default_sections("system"),
            priority=4,
        ))
        nav["Architecture"].append("middleware-auth")
        assigned_files.update(mw_files)

    # ── Feature buckets from domain dirs ─────────────────────────────────
    for group_name, files in sorted(domain_buckets_map.items()):
        if group_name in (".", "root") and len(files) <= 2:
            buckets[0].owned_files.extend(files)
            assigned_files.update(files)
            continue

        slug = f"{group_name.lower().replace(' ', '-').replace('_', '-')}"
        buckets.append(DocBucket(
            bucket_type="feature",
            title=f"{group_name.replace('_', ' ').replace('-', ' ').title()}",
            slug=slug,
            section="Features",
            description=f"Documentation for {group_name} feature area",
            owned_files=files,
            required_sections=_default_sections("feature"),
            priority=10,
        ))
        nav["Features"].append(slug)
        assigned_files.update(files)

    # ── Remaining role-based files ───────────────────────────────────────
    for role in ["controllers", "services", "utils", "routes", "config"]:
        files = [f for f in role_buckets_map.get(role, []) if f not in assigned_files]
        if not files:
            continue
        # Try to merge into an existing feature bucket
        merged = False
        for bucket in buckets:
            if bucket.bucket_type == "feature":
                overlap = set(f.split("/")[0] for f in files) & set(f.split("/")[0] for f in bucket.owned_files)
                if overlap:
                    bucket.owned_files.extend(files)
                    assigned_files.update(files)
                    merged = True
                    break
        if not merged:
            buckets.append(DocBucket(
                bucket_type="feature",
                title=f"{role.replace('_', ' ').title()}",
                slug=f"{role}",
                section="Modules",
                description=f"{role.title()} layer documentation",
                owned_files=files,
                required_sections=_default_sections("feature"),
                priority=15,
            ))
            nav["Modules"].append(role)
            assigned_files.update(files)

    # ── Endpoint bucket ──────────────────────────────────────────────────
    if scan.api_endpoints:
        resource_groups: dict[str, list[dict]] = defaultdict(list)
        for ep in scan.api_endpoints:
            path = ep.get("path", "")
            clean = re.sub(r"^/(?:api/)?(?:v\d+/)?", "", path)
            parts_list = [p for p in clean.split("/") if p and not p.startswith(":") and not p.startswith("{")]
            resource = parts_list[0] if parts_list else "general"
            resource_groups[resource].append(ep)

        for resource, eps in sorted(resource_groups.items()):
            ep_files = sorted(set(ep["file"] for ep in eps))
            slug = f"{resource}-api"
            buckets.append(DocBucket(
                bucket_type="endpoint",
                title=f"{resource.replace('_', ' ').replace('-', ' ').title()} API",
                slug=slug,
                section="API Reference",
                description=f"API reference for {resource} endpoints ({len(eps)} endpoints)",
                owned_files=ep_files,
                required_sections=_default_sections("endpoint"),
                priority=20,
            ))
            nav["API Reference"].append(slug)
            assigned_files.update(ep_files)

            # Individual endpoint_ref pages for each endpoint in this family
            for ep in eps:
                method = ep.get("method", "GET").upper()
                path = ep.get("path", "/unknown")
                handler = ep.get("handler", "")
                ep_file = ep.get("file", "")
                # Build a slug from method + path
                path_slug = re.sub(r"[/:{}]+", "-", path).strip("-").lower()
                ref_slug = f"{method.lower()}-{path_slug}"
                ref_title = f"{method} {path}"
                buckets.append(DocBucket(
                    bucket_type="endpoint_ref",
                    title=ref_title,
                    slug=ref_slug,
                    section="API Endpoints",
                    description=f"API reference for {method} {path} — handler: {handler}",
                    owned_files=[ep_file] if ep_file else [],
                    owned_symbols=[handler] if handler else [],
                    required_sections=_default_sections("endpoint_ref"),
                    priority=25,
                    depends_on=[slug],
                ))
                nav["API Endpoints"].append(ref_slug)

    # ── Skipped files ────────────────────────────────────────────────────
    all_source = set(scan.file_summaries.keys())
    test_files = sorted(f for f in all_source if any(
        p in f.split("/") for p in ("tests", "test", "__tests__", "spec")
    ))

    return DocPlan(
        buckets=buckets,
        nav_structure=dict(nav),
        skipped_files=test_files,
    )


