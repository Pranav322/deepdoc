@dataclass
class DocBucket:
    """A single documentation bucket — the core planning unit in v2."""

    bucket_type: str  # free-form label — LLM decides (e.g. "architecture", "feature", "cli-commands", "etl-stage")
    title: str
    slug: str
    section: str  # nav section: "Architecture", "Features", etc.
    description: str
    depends_on: list[str] = field(default_factory=list)  # other bucket slugs
    owned_files: list[str] = field(default_factory=list)  # source files
    owned_symbols: list[str] = field(
        default_factory=list
    )  # "ClassName.method" or "functionName"
    artifact_refs: list[str] = field(
        default_factory=list
    )  # config, env, deploy, test files
    required_sections: list[str] = field(default_factory=list)
    required_diagrams: list[str] = field(default_factory=list)
    coverage_targets: list[str] = field(default_factory=list)  # what must be covered
    generation_hints: dict[str, Any] = field(default_factory=dict)  # metadata flags for generator
    priority: int = 0
    parent_slug: str | None = None
    publication_tier: str = "core"
    source_kind_summary: dict[str, int] = field(default_factory=dict)


@dataclass
class DocPlan:
    """Complete doc plan generated by the multi-step planner."""

    buckets: list[DocBucket]
    nav_structure: dict[str, list[str]]  # section_name → [slug, slug, ...]
    skipped_files: list[str]
    orphaned_files: list[str] = field(default_factory=list)
    # Intermediate outputs preserved for debugging/persistence
    classification: dict[str, Any] = field(default_factory=dict)
    integration_candidates: list[dict] = field(default_factory=list)

    # Compatibility — pipeline_v2 currently reads .pages
    @property
    def pages(self) -> list[_BucketAsPage]:
        """Shim so existing pipeline_v2.py can treat buckets like pages."""
        return [_BucketAsPage(b) for b in self.buckets]


class _BucketAsPage:
    """Adapter so DocBucket looks like the old DocPage to existing code."""

    def __init__(self, bucket: DocBucket):
        self._b = bucket

    @property
    def title(self):
        return self._b.title

    @property
    def slug(self):
        return self._b.slug

    @property
    def page_type(self):
        return self._b.generation_hints.get("prompt_style", self._b.bucket_type)

    @property
    def description(self):
        return self._b.description

    @property
    def source_files(self):
        return self._b.owned_files

    @source_files.setter
    def source_files(self, val):
        self._b.owned_files = val

    @property
    def section(self):
        return self._b.section

    @property
    def priority(self):
        return self._b.priority

    @property
    def depends_on(self):
        return self._b.depends_on


@dataclass
class RepoScan:
    """Output of the scan phase — everything we know about the repo before calling the LLM."""

    file_tree: dict[str, list[str]]  # dir → [filenames]
    file_summaries: dict[str, str]  # rel_path → short summary (symbols, imports)
    api_endpoints: list[dict]  # serialized APIEndpoints
    languages: dict[str, int]  # language → file count
    has_openapi: bool
    openapi_paths: list[str]
    total_files: int
    frameworks_detected: list[str]
    entry_points: list[str]
    config_files: list[str]
    # New in v2 — Phase 1
    file_line_counts: dict[str, int] = field(
        default_factory=dict
    )  # rel_path → line count
    parsed_files: dict[str, ParsedFile] = field(
        default_factory=dict
    )  # rel_path → ParsedFile
    file_contents: dict[str, str] = field(
        default_factory=dict
    )  # rel_path → content (for integration scan)
    # New in v2 — Phase 2 (populated by run_phase2_scans)
    giant_file_clusters: dict[str, Any] = field(
        default_factory=dict
    )  # file_path → GiantFileAnalysis
    endpoint_bundles: list[Any] = field(default_factory=list)  # EndpointBundle list
    integration_identities: list[Any] = field(
        default_factory=list
    )  # IntegrationIdentity list
    artifact_scan: Any = None  # ArtifactScan
    doc_contexts: dict[str, str] = field(default_factory=dict)  # markdown/doc summaries
    research_contexts: list[dict[str, Any]] = field(default_factory=list)  # design/glossary/experiment context
    topic_candidates: list[dict[str, Any]] = field(default_factory=list)  # ranked topic hints
    semantic_file_token_cache: dict[str, set[str]] = field(default_factory=dict)
    topic_file_token_cache: dict[str, set[str]] = field(default_factory=dict)
    topic_context_token_cache: dict[str, set[str]] = field(default_factory=dict)
    planner_timings: dict[str, float] = field(default_factory=dict)
    source_kind_by_file: dict[str, str] = field(default_factory=dict)
    file_frameworks: dict[str, list[str]] = field(default_factory=dict)

    @property
    def published_api_endpoints(self) -> list[dict[str, Any]]:
        return [ep for ep in self.api_endpoints if ep.get("publication_ready", True)]


def tracked_bucket_files(bucket: DocBucket) -> list[str]:
    """Return all repo files whose changes should invalidate this bucket."""
    return sorted({f for f in [*bucket.owned_files, *bucket.artifact_refs] if f})


def endpoint_owned_files(endpoint: dict[str, Any]) -> list[str]:
    """Return route + handler ownership for an endpoint record."""
    files = [
        endpoint.get("route_file", ""),
        endpoint.get("handler_file", ""),
        endpoint.get("file", ""),
    ]
    return sorted({f for f in files if f})


