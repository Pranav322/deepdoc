"""V2 Multi-Step Planner — Bucket-Based Documentation Architecture.

Three-step planning pipeline:
  Step 1: CLASSIFY — categorize repo artifacts into source, setup, test, deploy,
          integration candidates, and cross-cutting/system areas
  Step 2: PROPOSE — create bucket candidates across system, feature, endpoint,
          and integration types
  Step 3: ASSIGN — map files/symbols/artifacts to buckets, produce final page plan

Four bucket types:
  - system:      architecture, setup, testing, deployment/ops, auth/middleware,
                 database layer, observability, shared cross-cutting concerns
  - feature:     business workflows (checkout, cancel/refund, order status, etc.)
  - endpoint:    endpoint or endpoint-family documentation
  - integration: external systems (Vinculum, Juspay, delivery partners, etc.)
"""

