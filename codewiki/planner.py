ENTRY_POINT_NAMES = {
    "main.py", "app.py", "server.py", "manage.py", "wsgi.py", "asgi.py",
    "index.ts", "index.js", "server.ts", "server.js", "app.ts", "app.js",
    "main.go", "cmd", "main.ts", "main.js",
    "artisan", "index.php",
}

CONFIG_FILE_PATTERNS = {
    "Dockerfile", "docker-compose.yml", "docker-compose.yaml",
    ".env.example", ".env.sample",
    "Makefile", "Taskfile.yml",
    "package.json", "tsconfig.json", "pyproject.toml", "setup.py", "setup.cfg",
    "go.mod", "composer.json",
    ".github", "Procfile", "vercel.json", "netlify.toml",
    "nginx.conf", "supervisord.conf",
}

FRAMEWORK_INDICATORS = {
    "express": ["express()", "require('express')", "from 'express'"],
    "fastify": ["fastify()", "require('fastify')", "from 'fastify'"],
    "nestjs": ["@nestjs/", "@Controller", "@Injectable"],
    "nextjs": ["next.config", "getServerSideProps", "getStaticProps"],
    "fastapi": ["from fastapi", "FastAPI()"],
    "flask": ["from flask", "Flask(__name__)"],
    "django": ["django.conf", "urlpatterns", "INSTALLED_APPS"],
    "laravel": ["Route::", "Illuminate\\", "artisan"],
    "gin": ["gin.Default()", "gin.New()", "gin.Engine"],
    "echo": ["echo.New()", "echo.Echo"],
    "fiber": ["fiber.New()", "fiber.App"],
    "falcon": ["falcon.App", "falcon.API"],
}


def scan_repo(repo_root: Path, cfg: dict[str, Any]) -> RepoScan:
    """Scan the entire repo without making any LLM calls."""
    exclude = cfg.get("exclude", [])
    include = cfg.get("include", [])
    extensions = supported_extensions()

    file_tree: dict[str, list[str]] = defaultdict(list)
    file_summaries: dict[str, str] = {}
    api_endpoints: list[dict] = []
    lang_counts: dict[str, int] = defaultdict(int)
    frameworks: set[str] = set()
    entry_points: list[str] = []
    config_files: list[str] = []
    openapi_paths: list[str] = []
    all_content_cache: dict[str, str] = {}

    ext_to_lang = {
        ".py": "python", ".js": "javascript", ".jsx": "javascript",
        ".ts": "typescript", ".tsx": "typescript",
        ".go": "go", ".php": "php",
        ".mjs": "javascript", ".cjs": "javascript",
    }

    # First pass: collect all files to get total count for progress bar
    all_files_to_scan: list[Path] = []
    for root, dirs, files in os.walk(repo_root):
        root_path = Path(root)
        dirs[:] = [d for d in dirs if not _matches_any(d, exclude)]
        for fname in sorted(files):
            fpath = root_path / fname
            rel = str(fpath.relative_to(repo_root))
            if not _matches_any(rel, exclude) and not _matches_any(fname, exclude):
                all_files_to_scan.append(fpath)

    with Progress(
        SpinnerColumn(),
        TextColumn("[progress.description]{task.description}"),
        BarColumn(),
        TaskProgressColumn(),
        console=console,
        transient=True,   # clears the bar when done
    ) as progress:
        task = progress.add_task("[dim]Scanning files...[/dim]", total=len(all_files_to_scan))

        for fpath in all_files_to_scan:
            root_path = fpath.parent
            fname = fpath.name
            rel = str(fpath.relative_to(repo_root))
            rel_dir = str(root_path.relative_to(repo_root)) if root_path != repo_root else "."

            progress.update(task, description=f"[dim]Scanning {rel}[/dim]")

            # Detect config files
            if fname in CONFIG_FILE_PATTERNS or any(p in rel for p in CONFIG_FILE_PATTERNS):
                config_files.append(rel)

            # Detect OpenAPI/Swagger specs
            if fname.lower() in ("openapi.json", "openapi.yaml", "openapi.yml",
                                  "swagger.json", "swagger.yaml", "swagger.yml"):
                openapi_paths.append(rel)

            # Only parse supported source files
            if fpath.suffix.lower() not in extensions:
                file_tree[rel_dir].append(fname)
                progress.advance(task)
                continue

            if include and not _matches_any(rel, include):
                progress.advance(task)
                continue

            file_tree[rel_dir].append(fname)

            lang = ext_to_lang.get(fpath.suffix.lower(), "")
            if lang:
                lang_counts[lang] += 1

            # Detect entry points
            if fname.lower() in ENTRY_POINT_NAMES or fname.lower().rstrip(".py.ts.js.go.php") in {"main", "app", "server", "index"}:
                entry_points.append(rel)

            # Parse file for summary
            try:
                content = fpath.read_text(encoding="utf-8", errors="replace")
                all_content_cache[rel] = content
            except Exception:
                progress.advance(task)
                continue

            # Detect frameworks
            for fw, indicators in FRAMEWORK_INDICATORS.items():
                if any(ind in content for ind in indicators):
                    frameworks.add(fw)

            # Parse symbols
            parsed = parse_file(fpath)
            if parsed:
                summary_parts = []
                if parsed.symbols:
                    sym_names = [f"{s.kind}:{s.name}" for s in parsed.symbols[:15]]
                    summary_parts.append(f"symbols=[{', '.join(sym_names)}]")
                if parsed.imports:
                    summary_parts.append(f"imports={len(parsed.imports)}")
                loc = len(content.splitlines())
                summary_parts.append(f"lines={loc}")
                file_summaries[rel] = " | ".join(summary_parts)

            # Detect API endpoints
            if lang:
                eps = detect_endpoints(fpath, content, lang)
                for ep in eps:
                    api_endpoints.append({
                        "method": ep.method,
                        "path": ep.path,
                        "handler": ep.handler,
                        "file": rel,
                        "line": ep.line,
                    })

            progress.advance(task)

    return RepoScan(
        file_tree=dict(file_tree),
        file_summaries=file_summaries,
        api_endpoints=api_endpoints,
        languages=dict(lang_counts),
        has_openapi=len(openapi_paths) > 0,
        openapi_paths=openapi_paths,
        total_files=sum(len(files) for files in file_tree.values()),
        frameworks_detected=sorted(frameworks),
        entry_points=entry_points,
        config_files=config_files,
    )


