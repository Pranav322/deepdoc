@dataclass
class DocPage:
    """A single page in the planned documentation."""
    title: str
    slug: str                       # filename: "authentication.md"
    page_type: str                  # overview | guide | api_reference | module | architecture | setup | integration
    description: str                # what this page covers
    source_files: list[str]         # which repo files to scan for this page
    section: str = ""               # nav section: "Getting Started", "API Reference", etc.
    priority: int = 0               # generation order (lower = first)
    depends_on: list[str] = field(default_factory=list)  # other page slugs this depends on


@dataclass
class DocPlan:
    """Complete doc plan generated by the AI."""
    pages: list[DocPage]
    nav_structure: dict[str, list[str]]     # section_name → [slug, slug, ...]
    skipped_files: list[str]                # files AI decided to skip
    orphaned_files: list[str] = field(default_factory=list)  # important files not assigned to any page


@dataclass
class RepoScan:
    """Output of the scan phase — everything we know about the repo before calling the LLM."""
    file_tree: dict[str, list[str]]         # dir → [filenames]
    file_summaries: dict[str, str]          # rel_path → short summary (symbols, imports)
    api_endpoints: list[dict]               # serialized APIEndpoints
    languages: dict[str, int]               # language → file count
    has_openapi: bool
    openapi_paths: list[str]
    total_files: int
    frameworks_detected: list[str]          # express, fastapi, laravel, gin, etc.
    entry_points: list[str]                 # main.py, index.ts, app.go, etc.
    config_files: list[str]                 # Dockerfile, docker-compose, .env.example, etc.


