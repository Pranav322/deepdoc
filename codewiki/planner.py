"""AI Doc Planner — the brain of CodeWiki.

Phase 1: Scan repo (no LLM) → file tree, symbols, endpoints, OpenAPI specs
Phase 2: Send scan to LLM → get back a full doc plan:
    - What pages to create (title, type, description)
    - Which source files feed into each page
    - Sidebar/nav structure
    - Files that can be skipped
Phase 3: Validate coverage — ensure no important files are orphaned

This replaces the hardcoded pipeline. The AI decides everything.
"""

