def _format_file_tree_compressed(tree: dict[str, list[str]], summaries: dict[str, str]) -> str:
    """Compress file tree to directory-level summaries so 300+ files fit.

    Format per directory:
      src/auth/ (6 files: auth.ts, middleware.ts, tokens.ts, +3 more)
        Key symbols: verifyToken, AuthMiddleware, generateJWT, RefreshToken
    """
    lines = []
    for dir_path in sorted(tree.keys()):
        files = tree[dir_path]
        if not files:
            continue

        # Count by extension
        ext_counts: dict[str, int] = defaultdict(int)
        for f in files:
            ext = f.rsplit(".", 1)[-1] if "." in f else "other"
            ext_counts[ext] += 1

        ext_summary = ", ".join(f"{count} .{ext}" for ext, count in sorted(ext_counts.items(), key=lambda x: -x[1]))
        shown = sorted(files)[:5]
        rest = len(files) - len(shown)
        file_list = ", ".join(shown) + (f", +{rest} more" if rest > 0 else "")

        lines.append(f"\n{dir_path}/ ({len(files)} files: {ext_summary})")
        lines.append(f"  Files: {file_list}")

        # Collect top symbols from this directory's files
        all_symbols: list[str] = []
        for rel_path, summary in summaries.items():
            file_dir = "/".join(rel_path.split("/")[:-1]) if "/" in rel_path else "."
            if file_dir == dir_path and "symbols=[" in summary:
                try:
                    sym_part = summary.split("symbols=[")[1].split("]")[0]
                    # Extract just the names (strip the kind: prefix)
                    names = [s.split(":")[-1].strip() for s in sym_part.split(",") if ":" in s]
                    all_symbols.extend(names[:5])
                except Exception:
                    pass

        if all_symbols:
            lines.append(f"  Key symbols: {', '.join(all_symbols[:12])}")

    return "\n".join(lines)


def _format_summaries_compressed(summaries: dict[str, str]) -> str:
    """Compressed per-file summaries grouped by directory.

    Each line: path (Nlines) — symbol1, symbol2, ...
    """
    lines: list[str] = []
    for path in sorted(summaries.keys()):
        summary = summaries[path]
        # Extract line count
        line_count = ""
        if "lines=" in summary:
            try:
                line_count = summary.split("lines=")[-1].split("|")[0].strip()
                line_count = f" ({line_count}L)"
            except Exception:
                pass
        # Extract symbol names (compact)
        symbols = ""
        if "symbols=[" in summary:
            try:
                sym_part = summary.split("symbols=[")[1].split("]")[0]
                names = [s.split(":")[-1].strip() for s in sym_part.split(",") if ":" in s]
                if names:
                    symbols = f" → {', '.join(names[:8])}"
                    if len(names) > 8:
                        symbols += f" +{len(names) - 8}"
            except Exception:
                pass
        lines.append(f"- {path}{line_count}{symbols}")
    return "\n".join(lines)


def _format_endpoints(endpoints: list[dict]) -> str:
    if not endpoints:
        return "(none)"
    lines = []
    for ep in endpoints[:50]:
        lines.append(f"- {ep['method']} {ep['path']} → {ep.get('handler', '?')} ({ep.get('file', '')}:{ep.get('line', 0)})")
    if len(endpoints) > 50:
        lines.append(f"... +{len(endpoints) - 50} more endpoints")
    return "\n".join(lines)


def _print_plan_summary(plan: DocPlan) -> None:
    table = Table(title="Documentation Plan", show_header=True, header_style="bold")
    table.add_column("#", style="dim", width=3)
    table.add_column("Page", style="cyan")
    table.add_column("Type", style="green")
    table.add_column("Section")
