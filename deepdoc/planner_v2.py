def _normalize_repo_rel_path(repo_root: Path, file_path: str) -> str:
    if not file_path:
        return ""
    path = Path(file_path)
    if not path.is_absolute():
        return path.as_posix()
    try:
        return path.relative_to(repo_root).as_posix()
    except ValueError:
        return path.as_posix()


DOC_CONTEXT_FILENAMES = {
    "readme.md",
    "readme.mdx",
    "changelog.md",
    "history.md",
    "design.md",
    "architecture.md",
    "glossary.md",
    "notes.md",
    "experiments.md",
}


def _is_doc_context_candidate(rel_path: str, file_name: str) -> bool:
    lower_rel = rel_path.lower()
    lower_name = file_name.lower()
    if lower_name in DOC_CONTEXT_FILENAMES:
        return True
    if lower_rel.startswith("docs/") and lower_name.endswith((".md", ".mdx", ".txt")):
        return True
    if any(token in lower_name for token in ("readme", "changelog", "glossary", "design", "experiment", "notes", "history")):
        return lower_name.endswith((".md", ".mdx", ".txt"))
    return lower_name.endswith(".ipynb")


def _summarize_doc_context(rel_path: str, content: str) -> tuple[str, dict[str, Any] | None]:
    lines = content.splitlines()
    headings = [line.strip().lstrip("# ").strip() for line in lines if line.strip().startswith("#")]
    nonempty = [line.strip() for line in lines if line.strip() and not line.strip().startswith("#")]
    summary_lines: list[str] = []
    if headings:
        summary_lines.append(f"Headings: {', '.join(headings[:8])}")
    if nonempty:
        summary_lines.append(f"Summary: {' '.join(nonempty[:3])[:300]}")
    summary = " | ".join(summary_lines)[:800]

    lower_rel = rel_path.lower()
    headings_blob = " ".join(headings).lower()
    kind = ""
    title = Path(rel_path).stem.replace("_", " ").replace("-", " ").title()
    if "glossary" in lower_rel:
        kind = "glossary"
    elif any(token in lower_rel for token in ("experiment", "ablation", "results")) or any(
        token in headings_blob for token in ("experiment", "ablation", "results")
    ):
        kind = "experiment_log"
    elif any(token in lower_rel for token in ("design", "architecture", "history")) or any(
        token in headings_blob for token in ("design", "architecture history", "history")
    ):
        kind = "design_history"
    elif any(token in lower_rel for token in ("note", "notes", "devlog", "development")) or any(
        token in headings_blob for token in ("development", "notes", "devlog")
    ):
        kind = "development_notes"
    if kind:
        return summary, {
            "kind": kind,
            "title": title,
            "file_path": rel_path,
            "summary": summary,
            "headings": headings[:12],
        }
    return summary, None


def _summarize_notebook_context(rel_path: str, content: str) -> tuple[str, dict[str, Any] | None]:
    try:
        notebook = json.loads(content)
    except Exception:
        return "", None
    markdown_cells: list[str] = []
    for cell in notebook.get("cells", []):
        if cell.get("cell_type") != "markdown":
            continue
        source = cell.get("source", [])
        if isinstance(source, list):
            markdown_cells.append("".join(source))
        elif isinstance(source, str):
            markdown_cells.append(source)
    if not markdown_cells:
        return "", None
    combined = "\n".join(markdown_cells[:8])
    summary, context = _summarize_doc_context(rel_path, combined)
    if context is None:
        lower_rel = rel_path.lower()
        if any(token in lower_rel for token in ("experiment", "ablation", "analysis", "notebook")):
            context = {
                "kind": "experiment_log",
                "title": Path(rel_path).stem.replace("_", " ").replace("-", " ").title(),
                "file_path": rel_path,
                "summary": summary,
                "headings": [],
            }
    return summary, context


def scan_repo(repo_root: Path, cfg: dict[str, Any]) -> RepoScan:
    """Scan the entire repo without making any LLM calls.

    Enhanced from v1: also records line counts and parsed files for
    giant-file detection and symbol-level bucket assignment.
    """
    exclude = cfg.get("exclude", [])
    include = cfg.get("include", [])
    extensions = supported_extensions()

    file_tree: dict[str, list[str]] = defaultdict(list)
    file_summaries: dict[str, str] = {}
    raw_api_endpoints: list[APIEndpoint] = []
    api_endpoints: list[dict] = []
    lang_counts: dict[str, int] = defaultdict(int)
    frameworks: set[str] = set()
    entry_points: list[str] = []
    config_files: list[str] = []
    openapi_paths: list[str] = []
    file_line_counts: dict[str, int] = {}
    parsed_files: dict[str, ParsedFile] = {}
    file_contents: dict[str, str] = {}
    doc_contexts: dict[str, str] = {}
    research_contexts: list[dict[str, Any]] = []
    source_kind_by_file: dict[str, str] = {}
    file_frameworks: dict[str, list[str]] = {}

    ext_to_lang = {
        ".py": "python",
        ".js": "javascript",
        ".jsx": "javascript",
        ".ts": "typescript",
        ".tsx": "typescript",
        ".go": "go",
        ".php": "php",
        ".mjs": "javascript",
        ".cjs": "javascript",
        ".vue": "vue",
    }

    # First pass: collect all files
    all_files_to_scan: list[Path] = []
    for root, dirs, files in os.walk(repo_root):
        root_path = Path(root)
        dirs[:] = [d for d in dirs if not _matches_any(d, exclude)]
        for fname in sorted(files):
            fpath = root_path / fname
            rel = str(fpath.relative_to(repo_root))
            if not _matches_any(rel, exclude) and not _matches_any(fname, exclude):
                all_files_to_scan.append(fpath)

    with Progress(
        SpinnerColumn(),
        TextColumn("[progress.description]{task.description}"),
        BarColumn(),
        TaskProgressColumn(),
        console=console,
        transient=True,
    ) as progress:
        task = progress.add_task(
            "[dim]Scanning files...[/dim]", total=len(all_files_to_scan)
        )

        for fpath in all_files_to_scan:
            fname = fpath.name
            rel = str(fpath.relative_to(repo_root))
            rel_dir = (
                str(fpath.parent.relative_to(repo_root))
                if fpath.parent != repo_root
                else "."
            )

            progress.update(task, description=f"[dim]Scanning {rel}[/dim]")
            source_kind_by_file[rel] = classify_source_kind(rel)

            # Detect config files
            if fname in CONFIG_FILE_PATTERNS or any(
                p in rel for p in CONFIG_FILE_PATTERNS
            ):
                config_files.append(rel)

            # Detect OpenAPI/Swagger specs
            if fname.lower() in (
                "openapi.json",
                "openapi.yaml",
                "openapi.yml",
                "swagger.json",
                "swagger.yaml",
                "swagger.yml",
            ):
                openapi_paths.append(rel)

            # Only parse supported source files
            if _is_doc_context_candidate(rel, fname):
                try:
                    doc_content = fpath.read_text(encoding="utf-8", errors="replace")
                    if fname.lower().endswith(".ipynb"):
                        summary, context = _summarize_notebook_context(rel, doc_content)
                    else:
                        summary, context = _summarize_doc_context(rel, doc_content)
                    if summary:
                        doc_contexts[rel] = summary
                    if context:
                        research_contexts.append(context)
                except Exception:
                    pass

            if fpath.suffix.lower() not in extensions:
                file_tree[rel_dir].append(fname)
                progress.advance(task)
                continue

            if include and not _matches_any(rel, include):
                progress.advance(task)
                continue

            file_tree[rel_dir].append(fname)

            lang = ext_to_lang.get(fpath.suffix.lower(), "")
            if lang:
                lang_counts[lang] += 1

            # Detect entry points
            if fname.lower() in ENTRY_POINT_NAMES or fname.lower().rstrip(
                ".py.ts.js.go.php"
            ) in {"main", "app", "server", "index"}:
                entry_points.append(rel)

            # Parse file for summary
            try:
                content = fpath.read_text(encoding="utf-8", errors="replace")
                line_count = len(content.splitlines())
                file_line_counts[rel] = line_count
                file_contents[rel] = content
            except Exception:
                progress.advance(task)
                continue

            # Detect frameworks
            matched_frameworks: list[str] = []
            for fw, indicators in FRAMEWORK_INDICATORS.items():
                if any(ind in content for ind in indicators):
                    frameworks.add(fw)
                    matched_frameworks.append(fw)
            if fpath.suffix.lower() == ".vue":
                frameworks.add("vue")
                matched_frameworks.append("vue")
            if fname.startswith("nuxt.config"):
                frameworks.add("nuxt")
                matched_frameworks.append("nuxt")
            if matched_frameworks:
                file_frameworks[rel] = sorted(set(matched_frameworks))

            # Parse symbols
            parsed = parse_file(fpath)
            if parsed:
                parsed_files[rel] = parsed
                summary_parts = []
                if parsed.symbols:
                    sym_names = [f"{s.kind}:{s.name}" for s in parsed.symbols[:15]]
                    summary_parts.append(f"symbols=[{', '.join(sym_names)}]")
                if parsed.imports:
                    summary_parts.append(f"imports={len(parsed.imports)}")
                summary_parts.append(f"lines={line_count}")
                file_summaries[rel] = " | ".join(summary_parts)

            # Detect API endpoints
            if lang:
                eps = detect_endpoints(fpath, content, lang)
                raw_api_endpoints.extend(eps)

            progress.advance(task)

    if raw_api_endpoints:
        resolved_endpoints = resolve_repo_endpoints(repo_root, raw_api_endpoints, file_contents)
        for ep in resolved_endpoints:
            if ep.path and ep.path.startswith("(see add_route for "):
                continue
            route_file = _normalize_repo_rel_path(repo_root, ep.route_file or ep.file)
            handler_file = _normalize_repo_rel_path(
                repo_root, ep.handler_file or ep.file or route_file
            )
            publication_ready, confidence, reason = endpoint_publication_decision(
                ep.path,
                route_file=route_file,
                handler_file=handler_file or route_file,
                framework=ep.framework,
                source_kind_by_file=source_kind_by_file,
            )
            api_endpoints.append(
                {
                    "method": ep.method,
                    "path": ep.path,
                    "handler": ep.handler,
                    "file": handler_file or route_file,
                    "route_file": route_file,
                    "handler_file": handler_file or route_file,
                    "line": ep.line,
                    "raw_path": ep.raw_path,
                    "framework": ep.framework,
                    "provenance": ep.provenance,
                    "source_kind": source_kind_by_file.get(handler_file or route_file, classify_source_kind(handler_file or route_file)),
                    "publication_ready": publication_ready,
                    "publication_confidence": confidence,
                    "publication_reason": reason,
                }
            )

    return RepoScan(
        file_tree=dict(file_tree),
        file_summaries=file_summaries,
        api_endpoints=api_endpoints,
        languages=dict(lang_counts),
        has_openapi=len(openapi_paths) > 0,
        openapi_paths=openapi_paths,
        total_files=sum(len(files) for files in file_tree.values()),
        frameworks_detected=sorted(frameworks),
        entry_points=entry_points,
        config_files=config_files,
        file_line_counts=file_line_counts,
        parsed_files=parsed_files,
        file_contents=file_contents,
        doc_contexts=doc_contexts,
        research_contexts=research_contexts,
        source_kind_by_file=source_kind_by_file,
        file_frameworks=file_frameworks,
    )


